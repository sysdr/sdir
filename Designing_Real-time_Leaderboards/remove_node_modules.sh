#!/bin/bash

# Script to remove node_modules directory that was created by Docker (owned by root)
# This script must be run with sudo

echo "Removing node_modules directory from leaderboard-demo/tests/..."
cd "$(dirname "$0")"

if [ -d "leaderboard-demo/tests/node_modules" ]; then
    rm -rf leaderboard-demo/tests/node_modules
    echo "✓ Successfully removed node_modules directory"
else
    echo "✓ node_modules directory not found (already removed)"
fi

